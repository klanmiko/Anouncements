package com.sst.anouncements;

class Announcement {
    public final String link;
    public final String title;
    public final String desc;
    public final String author;

    public Announcement(String link, String title, String desc, String author) {
        this.link = link;
        this.title = title;
        this.desc = desc;
        this.author = author;
    }

}
